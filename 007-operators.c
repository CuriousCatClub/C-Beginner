#include <stdio.h>

int main(void)
{
  int a, b;   // Both variables a, and b are int

  // Conditioal Operators
  a == b;               // True if a IS EQUAL to b - notice two equal signs ==
  a != b;               // True if a is NOT EQUAL to b
  a < b;                // True if a is LESS than b
  a > b;                // True if a is GREATER than b
  a <= b;               // True if a is LESS Than or EQUAL to b
  a >= b;               // True if a is GREATER than or EQUAL to b

  /* 
  Boolean Operators
  expr_a && expr_b      // Boolean AND
  expr_a || expt_b      // Boolean OR
  expr_a !  expr_b      // Boolean NOT         
  */

  return 0;
}
